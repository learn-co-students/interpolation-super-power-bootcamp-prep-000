def display_rainbow(rainbow_array = ['red', 'orange', 'yellow', 'green', 'blue', 'indigo', 'violet'])
  puts "R: #{rainbow_array[0]}, O: #{rainbow_array[1]}, Y: #{rainbow_array[2]}, G: #{rainbow_array[3]}, B: #{rainbow_array[4]}, I: #{rainbow_array[5]}, V: #{rainbow_array[6]}"
end