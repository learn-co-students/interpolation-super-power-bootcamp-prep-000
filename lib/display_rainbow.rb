def display_rainbow(array_colors)
  letters = ["R", "O", "Y", "G", "B", "I", "V"]
  print "#{letters[0]}: #{array_colors[0]}, "
  print "#{letters[1]}: #{array_colors[1]}, "
  print "#{letters[2]}: #{array_colors[2]}, "
  print "#{letters[3]}: #{array_colors[3]}, "
  print "#{letters[4]}: #{array_colors[4]}, "
  print "#{letters[5]}: #{array_colors[5]}, "
  print "#{letters[6]}: #{array_colors[6]}\n"
end
