def display_rainbow(arrayofcolors)
  puts "R: #{arrayofcolors[0]}, O: #{arrayofcolors[1]}, Y: #{arrayofcolors[2]}, G: #{arrayofcolors[3]}, B: #{arrayofcolors[4]}, I: #{arrayofcolors[5]}, V: #{arrayofcolors[6]}"
end

display_rainbow(['red', 'orange', 'yellow', 'green', 'blue', 'indigo', 'violet'])
