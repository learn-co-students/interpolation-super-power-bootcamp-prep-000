def display_rainbow(arr)
  first = ["R", "O", "Y", "G", "B", "I", "V"]
  
  out = "#{first[0]}: #{arr[0]}, #{first[1]}: #{arr[1]}, #{first[2]}: #{arr[2]}, #{first[3]}: #{arr[3]}, #{first[4]}: #{arr[4]}, #{first[5]}: #{arr[5]}, #{first[6]}: #{arr[6]}"
  
  puts(out)
end  