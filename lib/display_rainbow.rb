def display_rainbow(rainbow)
  print "#{rainbow[0][0].upcase}: #{rainbow[0]}, "
  print "#{rainbow[1][0].upcase}: #{rainbow[1]}, "
  print "Y: #{rainbow[2]}, "
  print "G: #{rainbow[3]}, "
  print "B: #{rainbow[4]}, "
  print "I: #{rainbow[5]}, "
  print "V: #{rainbow[6]}\n"
end