rainbow = ['red', 'orange', 'yellow', 'green', 'blue', 'indigo', 'violet']
def display_rainbow(rainbow)
  print "R: #{rainbow[0]}, "
  print "O: #{rainbow[1]}, "
  print "Y: #{rainbow[2]}, "
  print "G: #{rainbow[3]}, "
  print "B: #{rainbow[4]}, "
  print "I: #{rainbow[5]}, "
  print "V: #{rainbow[6]}\n"
end
