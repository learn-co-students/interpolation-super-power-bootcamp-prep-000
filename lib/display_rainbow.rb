def display_rainbow(color)
  print "R: #{color[0]}, "
  print "O: #{color[1]}, "
  print "Y: #{color[2]}, "
  print "G: #{color[3]}, "
  print "B: #{color[4]}, "
  print "I: #{color[5]}, "
  print "V: #{color[6]}"
end

display_rainbow(['red', 'orange', 'yellow', 'green', 'blue', 'indigo', 'violet'])
