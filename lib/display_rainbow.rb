def display_rainbow(rainbowarray)
  puts "R: #{rainbowarray[0]}, O: #{rainbowarray[1]}, Y: #{rainbowarray[2]}, G: #{rainbowarray[3]}, B: #{rainbowarray[4]}, I: #{rainbowarray[5]}, V: #{rainbowarray[6]}"
end

colors = ["red", "orange", "yellow", "green", "blue", "indigo", "violet"]
display_rainbow(colors)