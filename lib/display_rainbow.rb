def display_rainbow (rainbow)
  puts ("#{rainbow[0][0].upcase}: #{rainbow[0]}, #{rainbow[1][0].upcase}: #{rainbow[1]}, #{rainbow[2][0].upcase}: #{rainbow[2]}, #{rainbow[3][0].upcase}: #{rainbow[3]}, #{rainbow[4][0].upcase}: #{rainbow[4]}, #{rainbow[5][0].upcase}: #{rainbow[5]}, #{rainbow[6][0].upcase}: #{rainbow[6]}")
end

display_rainbow(['red', 'orange', 'yellow', 'green', 'blue', 'indigo', 'violet'])
